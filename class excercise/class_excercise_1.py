num_1 = int(input("Enter 1st number "))
num_2 = int(input("Enter 2nd number "))
num3 = int(input('Enter 3rd number '))

res_1 = num_1 + num_2 + num3
print("+",res_1)

res_2 = num_1-num_2 - num3
print("-",res_2)

res_3 = num_1*num_2 * num3
print("*",res_3)

res_4 = num_1**num_2
res_6 = res_4**num3
print("**",res_6)

res_5 = num_1/num_2
res_7 = res_5/num3
print("/",res_7)
